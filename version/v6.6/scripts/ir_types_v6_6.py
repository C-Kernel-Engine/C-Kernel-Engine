def _enum_member_name(value: str) -> str:
    """Convert op name to valid Python enum member name.

    Examples:
        "qkv_projection" → "QKV_PROJECTION"
        "attention" → "ATTENTION"
        "2d_slice" → "OP_2D_SLICE" (prefixed because starts with digit)
    """
    name = re.sub(r"[^a-zA-Z0-9]+", "_", value).upper()
    if not name:
        return "UNKNOWN"
    if name[0].isdigit():
        name = f"OP_{name}"
    return name


def _load_registry_json(path: Path) -> Dict:
    """Load kernel registry from JSON file.

    Args:
        path: Path to KERNEL_REGISTRY.json

    Returns:
        Parsed JSON as dict

    Raises:
        FileNotFoundError: If registry doesn't exist
    """
    if not path.exists():
        raise FileNotFoundError(
            f"Kernel registry not found: {path}. "
            "Run gen_kernel_registry_from_maps.py first."
        )
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def load_registry_ops(registry: Optional[Any] = None,
                      registry_path: Optional[Path] = None) -> List[str]:
    """Get sorted list of unique op names from kernel registry.

    Used to auto-generate the OpType enum.

    Returns:
        Sorted list like: ["attention", "gemm", "qkv_projection", ...]
    """
    if registry is None:
        registry_path = registry_path or DEFAULT_KERNEL_REGISTRY_PATH
        registry = _load_registry_json(registry_path)

    registry_map = normalize_kernel_registry(registry)
    ops = sorted({entry.get("op") for entry in registry_map.values() if entry.get("op")})
    if not ops:
        raise ValueError("Kernel registry contains no ops.")
    return ops


def build_op_type_enum(registry: Optional[Any] = None,
                       registry_path: Optional[Path] = None) -> Enum:
    """Build OpType enum dynamically from kernel registry.

    This ensures type safety - OpType only contains ops that exist in registry.
    Called at module import time (see end of file).

    Returns:
        Enum class with members: OpType.ATTENTION, OpType.GEMM, etc.
    """
    ops = load_registry_ops(registry=registry, registry_path=registry_path)
    members = {_enum_member_name(op): op for op in ops}
    return Enum("OpType", members)


def normalize_kernel_registry(registry: Any) -> Dict[str, Dict]:
    """Convert registry to standard {kernel_id: entry} format.

    Registry can come in different formats:
      1. Full format: {"kernels": [...], "_meta": {...}}
      2. List format: [...]
      3. Already normalized: {"kernel_id": {...}, ...}
      4. By-id format: {"by_id": {"kernel_id": {...}}}

    Returns:
        Normalized: {"kernel_id": {...kernel entry...}, ...}
    """
    if registry is None:
        return {}

    if isinstance(registry, list):
        return {k.get("name") or k.get("id"): k for k in registry if isinstance(k, dict)}

    if isinstance(registry, dict):
        if "kernels" in registry and isinstance(registry["kernels"], list):
            return {k.get("name") or k.get("id"): k for k in registry["kernels"] if isinstance(k, dict)}
        if "by_id" in registry and isinstance(registry["by_id"], dict):
            return registry["by_id"]
        if all(isinstance(v, dict) for v in registry.values()):
            return registry

    return {}


def make_buffer_name(layer_id: int, op_type: str, suffix: str = "") -> str:
    """Generate buffer name following v6.6 naming convention.

    v6.6 naming:
      - Global: "token_emb", "final_ln_gamma"
      - Layer: "layer.{layer_id}.{op_type}.{suffix}"

    Examples:
        make_buffer_name(0, "ln1")       → "layer.0.ln1"
        make_buffer_name(0, "ln1", "out") → "layer.0.ln1.out"
        make_buffer_name(5, "q")         → "layer.5.q"
    """
    if suffix:
        return f"layer.{layer_id}.{op_type}.{suffix}"
    return f"layer.{layer_id}.{op_type}"


def compute_buffer_size(shape: List[str], dtype: str, dims: Dict[str, int]) -> int:
    """Calculate buffer size in bytes given symbolic shape and concrete dimensions.

    Examples:
        # FP32 tensor
        compute_buffer_size(["T", "AE"], "fp32", {"T": 512, "AE": 2048})
        # Returns: 512 * 2048 * 4 = 4,194,304 bytes

        # Q5_0 quantized tensor
        compute_buffer_size(["H*AD", "AE"], "q5_0", {"H*AD": 896, "AE": 896})
        # Returns: 896 * 896 * 0.6875 ≈ 551,936 bytes

    Used during layout planning to allocate buffers.
    """
    elements = 1
    for s in shape:
        if isinstance(s, int):
            elements *= s
        elif s in dims:
            elements *= dims[s]
        else:
            raise ValueError(f"Unknown dimension: {s}")

    if dtype.startswith("q"):
        # Quantized - use block size
        block_size = QUANT_BLOCK.get(dtype, 32)
        n_blocks = (elements + block_size - 1) // block_size
        # Approximate bytes per element based on format
        bytes_per_elem = DTYPE_BYTES.get(dtype, 0.5)
        return int(elements * bytes_per_elem)
    else:
        # Native dtype
        elem_bytes = DTYPE_BYTES.get(dtype, 4)
        return elements * elem_bytes


def resolve_kernel_template(
    op_type: str,
    weight_dtype: str,
    registry: Optional[Dict] = None
) -> str:
    """Find kernel ID matching (op_type, weight_dtype).

    This is the CORE kernel selection function.
    Maps (op_type, weight_dtype) → kernel_id

    Examples:
        resolve_kernel_template("gemm", "q5_0")
        # Returns: "gemm_nt_q5_0_q8_0"

        resolve_kernel_template("gemm", "q4_k")
        # Returns: "gemm_nt_q4_k_q8_k"

    How it works:
        1. Iterate through all kernels in registry
        2. Filter by op_type
        3. Check weight dtype compatibility (exact, "mixed", or "q5_0|q8_0" list)
        4. Return first matching kernel

    Called during "lowering" stage to select appropriate kernel.
    """
    registry_map = normalize_kernel_registry(registry)
    if not registry_map:
        raise ValueError("Kernel registry required for resolve_kernel_template()")

    weight_dtype = (weight_dtype or "").strip().lower()
    candidates = []

    for kernel_id, entry in registry_map.items():
        if entry.get("op") != op_type:
            continue
        candidates.append(kernel_id)
        quant = entry.get("quant", {}) or {}
        weight_support = (quant.get("weight", "") or "").lower()
        if not weight_dtype:
            if weight_support in ("", "none"):
                return kernel_id
            continue
        if weight_support == "mixed":
            return kernel_id
        if weight_support == weight_dtype:
            return kernel_id
        if "|" in weight_support:
            parts = [p.strip() for p in weight_support.split("|") if p.strip()]
            if weight_dtype in parts:
                return kernel_id

    raise ValueError(
        f"No kernel in registry for op='{op_type}' weight_dtype='{weight_dtype}'\n"
        f"  Available: {candidates}"
    )


