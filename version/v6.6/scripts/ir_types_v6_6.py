# SECTION 5: HELPER FUNCTIONS
# =============================================================================
#
# Utility functions used throughout the pipeline.
# All functions are stateless - they don't modify any global state.
#
# Function categories:
#   - Registry loading: _load_registry_json, load_registry_ops, build_op_type_enum
#   - Registry normalization: normalize_kernel_registry
#   - Buffer utilities: make_buffer_name, compute_buffer_size
